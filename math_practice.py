#showing how to do basic math in python
import math #import math module

#addition
add = 2 + 2 #just use plus sign
print(add)

#subtraction
sub = 4-2 #just use minus sign
print(sub)

#multiplication
mult = 15*3 #use the asterisk
print(mult)

#division
div = 15/3 #use forward slash
print(div)

#exponents
exp = 4**4 #use two asterisks
print(exp)

#modulo i.e. the remainder of something divided
mod = 13%2 #you should have one left over...
print(mod)

test_1 = div * math.pi * 2 #this is multiplying "pi" by two
print(test_1)
