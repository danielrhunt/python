#practicing definite loops

#if you want a loop to sift through a set of things, you use a definite loop
#this means the loop is defined, i.e. it's finite (unlike While, which is basically infinite)
#use the FOR statement for definite loops
#the for statement can cycle through strings, integers, or floating point numbers

family = ["Claire", "Caitlin", "Tara"]
for i in family:
    print("You're the best,", i, ", and I love you!")
print("Done!")
